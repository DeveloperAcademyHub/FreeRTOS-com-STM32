void TaskSend(void *pvParams){
	uint32_t u32Send = 0;

	while (1){
		// retorna a quantidade mínima de espaço livre que restou na pilha
		xWaterMark01 = uxTaskGetStackHighWaterMark( NULL );

		// Armazenando o valor na fila
		if ( (xQueueSend(xQueue, &u32Send, (TickType_t) 0) ) == pdFAIL){
			Error_Handler();
		}

		u32Send++;
		LedOn();
		vTaskDelay(pdMS_TO_TICKS(1000));

	}
}

